package my.day05.d.SWITCH;

import java.util.Scanner;

public class SwitchMain {

	public static void main(String[] args) {
	
		Scanner sc = new Scanner(System.in);
		
		try {
			
			System.out.print("첫번째 정수 입력 => ");
			int num1 = Integer.parseInt(sc.nextLine());
			
			
			System.out.print("두번째 정수 입력 => ");
			int num2 = Integer.parseInt(sc.nextLine());
			System.out.print("사칙연산자 선택 (+ - * /) => ");
			String operator = sc.nextLine();
			
			String result = "";
			
            //== switch 문 == //
			switch (operator) { 
			case "+":  //operator의 값이 "+"와 같다라면
				result = String.valueOf(num1 + num2);
				break; //switch문을 빠져나간다
				
			case "-":  //operator의 값이 "-"와 같다라면
				result = String.valueOf(num1 - num2);
				break;
				
			case "*":  //operator의 값이 "*"와 같다라면
				result = String.valueOf(num1 * num2);
				break;	
				
			case "/":  //operator의 값이 "/"와 같다라면
				if(num2 == 0) 
					result = ">>> 분모에는 0 이 올 수 없습니다! <<<";
				else
					result = String.valueOf((double)num1 / num2);
				break;

			default:  //operator의 값이 사칙연산 부호가 아니라면
				System.out.println(">> 사칙연산(+ - * /)만 선택하세요. <<"); 
				sc.close();
				break;
				//if문과 달리 {}가 없음
			} //end of switch -----------------------------------------------------------------
					
			//***삼항연산자
			result = ("/".equals(operator) && num2==0)? result : num1+"+"+num2+"="+result;
			
			System.out.println(result);
			
		} catch (NumberFormatException e) {	
			
			System.out.println(">>> 정수로 입력해주세요! <<<");
			e.getStackTrace();
						
		}

		sc.close();

	}//end of main

}
